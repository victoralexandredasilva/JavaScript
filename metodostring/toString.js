//Number for String
const number = 2;
const numberString = number.toString();
console.log(typeof numberString); // String

//Array for String
const arr = [ 1, 3 , 5, 6 , 9];
const arrString = arr.toString();
console.log(typeof arrString); // String

//Boolean for String
const bool = true; 
const booleanString = bool.toString();
console.log(typeof booleanString); //String

//Object for string
const obj = {a: 1, b: 2, c:3};
const objString = obj.toString();
console.log(typeof objString); //String